#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	number_to_print;

	if (n == -2147483648)
	{
		ft_putstr_fd("-2", fd);
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = n * -1;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	number_to_print = '0' + n % 10;
	ft_putchar_fd(number_to_print, fd);
}
